function linePoints(x1, y1, x2, y2)
  return { x1 = x1, y1 = y1, x2 = x2, y2 = y2 }
end

function addline(lineList,x1, y1, x2, y2)
  table.insert(lineList,linePoints((x1*picScale),(y1*picScale),(x2*picScale),(y2*picScale)))
end

